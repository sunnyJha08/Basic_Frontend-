// Assignment Operators in JavaScript

let a = 5;

a += 3; // Equivalent to a = a + 3
console.log("+= result:", a); // 8

a -= 2; // a = a - 2
console.log("-= result:", a); // 6

a *= 2; // a = a * 2
console.log("*= result:", a); // 12

a /= 3; // a = a / 3
console.log("/= result:", a); // 4

a %= 2; // a = a % 2
console.log("%= result:", a); // 0

// Works with strings too
let str = "Hello";
str += " World";
console.log("String += result:", str); // "Hello World"
