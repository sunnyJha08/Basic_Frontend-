// for-loop.js

// The 'for' loop is useful when you know in advance how many times you want to loop

// Syntax:
// for (initialization; condition; increment/decrement) {
//     // code to be executed
// }

for (let i = 1; i <= 5; i++) {
  // This loop will run 5 times
  // i starts at 1 and increases by 1 until it reaches 5
  console.log("For Loop Iteration: " + i);
  // Output: For Loop Iteration: 1 ... up to 5
}
