// Basic arithmetic operations in JavaScript

let a = 10;
let b = 3;

console.log("Addition:", a + b); // 13
console.log("Subtraction:", a - b); // 7
console.log("Multiplication:", a * b); // 30
console.log("Division:", a / b); // 3.333...
console.log("Modulus (remainder):", a % b); // 1

// Increment and Decrement
let x = 5;
console.log("Post-increment:", x++); // Prints 5, then x becomes 6
console.log("Now x is:", x); // 6
console.log("Pre-decrement:", --x); // Decreases x to 5, then prints
